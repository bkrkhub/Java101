
public class MathClass4 {

	public static void main(String[] args) {
		// Math Sınıfı Metotları
		// ceil -> bir büyük tam sayıya yuvarlar
		// cos -> cosinus alır.
		// sin -> sin alır.
		// floor -> küçük tam sayıya yuvarlar
		// max -> büyük sayıyı verir.
		// min -> küçük sayıyı verir.
		// pow -> üs alır
		// random -> 1 ve 0 arasında rastgele değer
		// round -> yakın olan tam sayıya döner
		// abs -> mutlak değer alır.
		
		System.out.println(Math.ceil(1.1));
		System.out.println(Math.cos(30));
		System.out.println(Math.floor(1.1));
		System.out.println(Math.max(1.1,2));
		System.out.println(Math.pow(2,3));
		System.out.println(Math.round(1.4));
		System.out.println(Math.abs(-22));
		System.out.println(Math.sqrt(16));
	}

}
